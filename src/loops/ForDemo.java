package loops;

class ForDemo { 
	
	public static void main(String[] args) {
        
    	/*
    	 * Basic for loop structure:
    	 * 
    	 *   for (initialization; termination; increment) {
    	 * 	  	  statement(s)
    	 *   }
    	 */
    	for (int i = 1; i <= 1000; i++){
    		System.out.println("Count is: " + i);
    	
    	}
    }
}